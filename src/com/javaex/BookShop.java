package com.javaex;

import java.util.Scanner;
public class BookShop {
    public static void main(String[] args) {
        Book[] books = new Book[10];
        Scanner sc = new Scanner(System.in);
        books[0] = new Book(1, "트와일라잇", "스테파니메이어",1);
        books[1] = new Book(2, "뉴문", "스테파니메이어",1);
        books[2] = new Book(3, "이클립스", "스테파니메이어",1);
        books[3] = new Book(4, "브레이킹던", "스테파니메이어",1);
        books[4] = new Book(5, "아리랑", "조정래",1);
        books[5] = new Book(6, "젊은그들", "김동인",1);
        books[6] = new Book(7, "아프니깐 청춘이다", "김난도",0);
        books[7] = new Book(8, "귀천", "천상병",1);
        books[8] = new Book(9, "태백산맥", "조정래",1);
        books[9] = new Book(10, "풀하우스", "원수연",1);

        System.out.println("*****도서 정보 출력하기******");
        displayBookInfo(books);
        
        
        Scanner scanner = new Scanner(System.in);
        System.out.print("대여 하고 싶은 책의 번호를 입력하세요:");
        int num = scanner.nextInt();
        scanner.close();
        
        if(num<1 || num>books.length) {
        	System.out.println("존재하지 않는 책번호 입니다.");
        }
        for(int i=0;i<books.length; i++)  {
        	if(books[i].getBookNo() == num)  {
        		books[i].rent(num);
        	}
        }
        System.out.println("*****도서 정보 출력하기******");
        displayBookInfo(books);	
    }
        
    //(2)전달받은 배열을 모두 출력하는 메소드
    private static void displayBookInfo(Book[] bo) {
        //코드작성
       
        for(int i=0;i<bo.length; i++)  {
        	bo[i].print();
        }
	}
       
}